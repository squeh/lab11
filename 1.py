for n in range(1, 13):
    for k in range(1, 13):
        for m in range(1, 13):
            if 28*n + 30*k + 31*m == 365:
                print(f"""
                 Кол-во месяцев по 28 дней = {n},
                 Кол-во месяцев по 30 дней = {k},
                 Кол-во месяцев по 31 дню = {m}""")
